def get_client_translations(lang: str | None = None) -> dict[str, str]:
    """Return the translation dictionary exposed to the browser."""
    active_lang = lang or get_current_language()
    if active_lang == "en":
        return EN_TRANSLATIONS
    return {}


def get_current_language() -> str:
    """Return the current UI language code."""
    if not has_request_context():
        return "fr"

    lang = request.args.get("lang")
    if lang in SUPPORTED_LANGUAGES:
        session["lang"] = lang
        return lang

    lang = session.get("lang")
    if lang in SUPPORTED_LANGUAGES:
        return lang

    best = request.accept_languages.best_match(list(SUPPORTED_LANGUAGES))
    return best or "fr"


def t(text: str, lang: str | None = None) -> str:
    """Translate a UI string from FR to EN, fallback to source text."""
    active_lang = lang or get_current_language()
    if active_lang == "en":
        return EN_TRANSLATIONS.get(text, text)
    return text